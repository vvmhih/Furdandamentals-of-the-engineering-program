# Ввод показаний счётчика
prev = int(input("Введите предыдущее показание: "))
curr = int(input("Введите текущее показание: "))

# Определение объёма использованного газа
if curr >= prev:
    volume = curr - prev
else:
    volume = 10000 - prev + curr

print(f"Объем использованного газа: {volume} куб. м")

# Расчёт стоимости по тарифным зонам
if volume <= 300:
    # Только фиксированная плата
    cost = 21.0
elif volume <= 600:
    # Фиксированная плата + 0.06$ за кубометр сверх 300
    cost = 21 + (volume - 300) * 0.06
elif volume <= 800:
    # Фиксированная плата + 300 куб.м по 0.06$ + сверх 600 по 0.04$
    cost = 21 + 300 * 0.06 + (volume - 600) * 0.04
else:
    # Фиксированная плата + 300 по 0.06$ + 200 по 0.04$ + сверх 800 по 0.025$
    cost = 21 + 300 * 0.06 + 200 * 0.04 + (volume - 800) * 0.025

# Расчёт средней цены
avg_price = cost / volume if volume > 0 else 0

# Вывод с форматированием
print(f"Сумма к оплате: ${cost:.2f}")
print(f"Средняя цена за кубометр: ${avg_price:.2f}")